# BMI Calculator

This is a simple BMI Calculator built using **HTML**, **CSS**, and **JavaScript**. It allows users to input their height and weight, calculates their Body Mass Index (BMI), and displays the result along with a health category based on the BMI value.

## Features

- Input fields for height (in meters) and weight (in kilograms).
- Calculation of BMI using the formula:  
  \[ \text{BMI} = \frac{\text{weight (kg)}}{\text{height (m)}^2} \]
- Display of the calculated BMI and corresponding health category:
  - Underweight: BMI < 18.5
  - Normal weight: 18.5 ≤ BMI < 24.9
  - Overweight: 25 ≤ BMI < 29.9
  - Obesity: BMI ≥ 30

## Technologies Used

- **HTML**: For structuring the user interface.
- **CSS**: For styling the calculator.
- **JavaScript**: For handling user input and performing calculations.

